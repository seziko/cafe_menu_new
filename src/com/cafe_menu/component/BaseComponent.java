package com.cafe_menu.component;

public class BaseComponent {

    String ekle;
    String degistir;
    String kaydet;
    String kullaniciAdi;
    String kullaniciSifre;

    public String getEkle() {
        return ekle;
    }

    public void setEkle(String ekle) {
        this.ekle = ekle;
    }

    public String getDegistir() {
        return degistir;
    }

    public void setDegistir(String degistir) {
        this.degistir = degistir;
    }

    public String getKaydet() {
        return kaydet;
    }

    public void setKaydet(String kaydet) {
        this.kaydet = kaydet;
    }

    public String getKullaniciAdi() {
        return kullaniciAdi;
    }

    public void setKullaniciAdi(String kullaniciAdi) {
        this.kullaniciAdi = kullaniciAdi;
    }

    public String getKullaniciSifre() {
        return kullaniciSifre;
    }

    public void setKullaniciSifre(String kullaniciSifre) {
        this.kullaniciSifre = kullaniciSifre;
    }
}
