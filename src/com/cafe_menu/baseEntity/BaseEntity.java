package com.cafe_menu.baseEntity;

public class BaseEntity {

    Integer id;
    String ekul;
    String etar;
    String dkul;
    String dtar;


    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getEkul() {
        return ekul;
    }

    public void setEkul(String ekul) {
        this.ekul = ekul;
    }

    public String getEtar() {
        return etar;
    }

    public void setEtar(String etar) {
        this.etar = etar;
    }

    public String getDkul() {
        return dkul;
    }

    public void setDkul(String dkul) {
        this.dkul = dkul;
    }

    public String getDtar() {
        return dtar;
    }

    public void setDtar(String dtar) {
        this.dtar = dtar;
    }
}
